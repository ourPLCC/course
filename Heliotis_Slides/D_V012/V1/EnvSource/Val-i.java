/**
 * The run-time value of a variable
 */
public class Val {

    /**
     * The actual int value
     */
    public final int value;

    /**
     * Create an int Val.
     */
    public Val( int value ) {
        this.value = value;
    }

    /**
     * Return the int value as a string.
     */
    @Override
    public String toString() {
        return Integer.toString( value );
    }
}
